export default function Map({ lat, lng }) {
  const { isLoaded } = useLoadScript({
    googleMapsApiKey: process.env.NEXT_PUBLIC_GOOGLE_API, // Ensure this is defined in your .env file
  });

  if (!isLoaded)
    return (
      <div className="h-96 w-full rounded-xl mb-6 relative">
        {<SmallSpinner className={"rounded-xl"} />}
      </div>
    );

  const center = {
    lat: Number(lat),
    lng: Number(lng),
  };

  return (
    <GoogleMap
      mapContainerStyle={containerStyle}
      mapContainerClassName="rounded-xl w-full mb-6 "
      center={center}
      zoom={15}
      options={{
        styles: mapStyles, // Apply the custom map styles
        disableDefaultUI: true, // Disable default UI (optional)
      }}
    >
      {/* Example Marker */}
      <Marker
        position={center}
        icon={{
          url: "/place_icon.svg", // Replace with your marker image path
          scaledSize: new window.google.maps.Size(40, 40), // Adjust size
        }}
      />
    </GoogleMap>
  );
}
